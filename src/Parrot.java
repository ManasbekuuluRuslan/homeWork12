public class Parrot {
   private String color;
   private int age;
   private  int from;

    public void setColor(String color) {
        this.color = color;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setFrom(int from) {
        this.from = from;
    }

    public String getColor() {
        return color;
    }

    public int getAge() {
        return age;
    }

    public int getFrom() {
        return from;
    }
}
